//   A diferença essencial entre a LinkedQueue e a CircularArrayQueue é que a fila linear consome mais espaço que a fila circular, 
//enquanto a fila circular foi criada para limitar o desperdício de memória da fila linear.
//
//---------LinkedQueue---------
//- Organiza os elementos de dados e instruções em uma ordem seqüencial, um após o outro.
//- As tarefas são executadas na ordem em que foram colocadas antes (FIFO).
//- O novo elemento é adicionado pela extremidade traseira e removido pela frente.
//- Performance ineficiente
//
//
//--------CircularArrayQueue-----
//- Organiza os dados no padrão circular em que o último elemento está conectado ao primeiro elemento
//- A ordem de execução de uma tarefa pode mudar.
//- A inserção e exclusão podem ser feitas em qualquer posição.
//- Funciona melhor que a fila linear.

